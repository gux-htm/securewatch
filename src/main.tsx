import React from 'react';
import ReactDOM from 'react-dom/client';
import App from './App';
import './index.css';

class ErrorBoundary extends React.Component<
  { children: React.ReactNode },
  { error: Error | null }
> {
  constructor(props: { children: React.ReactNode }) {
    super(props);
    this.state = { error: null };
  }
  static getDerivedStateFromError(error: Error) {
    return { error };
  }
  render() {
    if (this.state.error) {
      return (
        <div style={{ background: '#0D1117', color: '#FF4444', fontFamily: 'monospace', padding: '2rem', minHeight: '100vh' }}>
          <h1 style={{ color: '#E6EDF3', marginBottom: '1rem' }}>SecureWatch — Startup Error</h1>
          <pre style={{ whiteSpace: 'pre-wrap', wordBreak: 'break-all', background: '#161B22', padding: '1rem', borderRadius: '6px', border: '1px solid #FF4444' }}>
            {this.state.error.message}
            {'\n\n'}
            {this.state.error.stack}
          </pre>
        </div>
      );
    }
    return this.props.children;
  }
}

const rootElement = document.getElementById('root');
if (!rootElement) throw new Error('Root element not found');

ReactDOM.createRoot(rootElement).render(
  <React.StrictMode>
    <ErrorBoundary>
      <App />
    </ErrorBoundary>
  </React.StrictMode>
);
